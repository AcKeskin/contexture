function inferModuleRole(modName, items) {
  const hardcoded = ROLE_BY_MODULE_NAME[modName];
  if (hardcoded) return hardcoded;
  // Fallback: derive from highest-inbound-edge file's purpose, else humanize the folder name.
  if (items.length) {
    const sorted = [...items].sort((a, b) => (b.inboundCount || 0) - (a.inboundCount || 0));
    if (sorted[0] && sorted[0].purpose && sorted[0].purpose !== '<vendored>') return sorted[0].purpose;
  }
  return modName.replace(/\/$/, '').replace(/[-_]+/g, ' ');
}

function inferModulePurpose(items, inboundCounts) {
  // 2-3 most-imported files' purposes, concatenated. Falls back to top-by-export-count.
  const ranked = items
    .map((it) => ({ ...it, _in: inboundCounts.get(it.rel) || 0 }))
    .filter((it) => it.purpose && it.purpose !== '<vendored>')
    .sort((a, b) => b._in - a._in || (b.exports?.length || 0) - (a.exports?.length || 0));
  const top = ranked.slice(0, 3).map((it) => it.purpose);
  // Dedupe purpose lines; some files share the same one-liner.
  const seen = new Set();
  const unique = [];
  for (const p of top) {
    const k = p.toLowerCase();
    if (seen.has(k)) continue;
    seen.add(k);
    unique.push(p);
  }
  return unique.join('. ');
}

function findModuleEntry(modName, items, entryPoints) {
  const inModule = entryPoints.find((e) => topLevelDirOf(e.rel) === modName);
  if (inModule) return inModule.rel;
  const candidates = ['index.ts', 'index.js', 'index.mjs', 'Boot.cs', 'Main.cs', 'Program.cs'];
  for (const c of candidates) {
    const direct = items.find((it) => path.posix.basename(it.rel) === c && it.rel.split('/').length === 2);
    if (direct) return direct.rel;
  }
  return '';
}

function computeInboundCounts(fileEdges) {
  const inbound = new Map();
  for (const targets of fileEdges.values()) {
    for (const t of targets) inbound.set(t, (inbound.get(t) || 0) + 1);
  }
  return inbound;
}

function computeModuleImportedBy(moduleEdges) {
  const inv = new Map();
  for (const [from, bucket] of moduleEdges.entries()) {
    for (const to of bucket.keys()) {
      if (!inv.has(to)) inv.set(to, new Set());
      inv.get(to).add(from);
    }
  }
  return inv;
}

function publicSurfaceForModule(items, inboundCounts) {
  // Rank files by inbound, take exports[0..2] from each. Cap at 10 total entries.
  const ranked = [...items]
    .filter((it) => it.exports && it.exports.length)
    .map((it) => ({ ...it, _in: inboundCounts.get(it.rel) || 0 }));
  const hasEdges = ranked.some((it) => it._in > 0);
  ranked.sort((a, b) => hasEdges
    ? (b._in - a._in) || a.rel.localeCompare(b.rel)
    : a.rel.localeCompare(b.rel));
  const surface = [];
  for (const it of ranked) {
    for (const e of it.exports.slice(0, 3)) {
      // Trim signature tail (` — sig`) to keep public surface readable.
      const name = e.name.replace(/\s+—\s+.*$/, '').replace(/^default:\s+/, '');
      surface.push(name);
      if (surface.length >= 10) break;
    }
    if (surface.length >= 10) break;
  }
  // Dedupe while preserving order.
  const seen = new Set();
  return surface.filter((s) => { if (seen.has(s)) return false; seen.add(s); return true; });
}

function computeInternals(modName, items, inboundCounts) {
  // Group files by immediate sub-folder (between modName/ and basename).
  // Emit only sub-folders with ≥2 files.
  const bySubfolder = new Map();
  for (const it of items) {
    const rest = it.rel.slice(modName.length); // strips "module/"
    const parts = rest.split('/');
    if (parts.length < 2) continue; // file is directly in module/
    const sub = parts[0];
    if (!bySubfolder.has(sub)) bySubfolder.set(sub, []);
    bySubfolder.get(sub).push(it);
  }
  const lines = [];
  const sortedSubs = [...bySubfolder.keys()].sort();
  for (const sub of sortedSubs) {
    const subItems = bySubfolder.get(sub);
    if (subItems.length < 2) continue;
    const topItem = [...subItems].sort((a, b) => (inboundCounts.get(b.rel) || 0) - (inboundCounts.get(a.rel) || 0))[0];
    const purpose = topItem && topItem.purpose && topItem.purpose !== '<vendored>'
      ? topItem.purpose
      : `${subItems.length} files`;
    lines.push(`${modName}${sub}/ — ${purpose}`);
  }
  return lines;
}

function detectConventions(groups) {
  // Evidence-based: only fire for ≥3-instance explicit markers (implements/extends/attribute).
  const impl = new Map();   // interface -> [{name, file}]
  const ext = new Map();    // base class -> [{name, file}]
  const attr = new Map();   // attribute -> [{name, file}]

  for (const items of groups.values()) {
    for (const it of items) {
      if (!it.classes) continue;
      for (const c of it.classes) {
        for (const i of (c.implements || [])) {
          const key = normalizeTypeRef(i);
          if (!impl.has(key)) impl.set(key, []);
          impl.get(key).push({ name: c.name, file: it.rel });
        }
        for (const e of (c.extends || [])) {
          const key = normalizeTypeRef(e);
          if (!ext.has(key)) ext.set(key, []);
          ext.get(key).push({ name: c.name, file: it.rel });
        }
        for (const a of (c.attributes || [])) {
          if (!attr.has(a)) attr.set(a, []);
          attr.get(a).push({ name: c.name, file: it.rel });
        }
      }
    }
  }

  const conventions = [];
  const examplesOf = (entries) => entries.slice(0, 3).map((e) => `\`${e.name}\``).join(', ');

  for (const [iface, entries] of impl.entries()) {
    if (entries.length < 3) continue;
    if (!iface) continue;
    conventions.push({
      kind: 'implements',
      name: `Implements ${iface}`,
      count: entries.length,
      detail: `tool/handler convention, e.g. ${examplesOf(entries)}`,
    });
  }
  for (const [base, entries] of ext.entries()) {
    if (entries.length < 3) continue;
    if (!base) continue;
    if (/^(Error|Exception|object)$/.test(base)) continue;
    conventions.push({
      kind: 'extends',
      name: `Extends ${base}`,
      count: entries.length,
      detail: `subclass family, e.g. ${examplesOf(entries)}`,
    });
  }
  for (const [a, entries] of attr.entries()) {
    if (entries.length < 3) continue;
    conventions.push({
      kind: 'attribute',
      name: `Carries [${a}] attribute`,
      count: entries.length,
      detail: `reflection-discoverable marker, e.g. ${examplesOf(entries)}`,
    });
  }

  conventions.sort((a, b) => b.count - a.count || a.name.localeCompare(b.name));
  return conventions;
}

async function main() {
  const args = parseArgs(process.argv);
  const root = args.root;
  if (!fs.existsSync(root)) { console.error(`root not found: ${root}`); process.exit(1); }

  log(args, `root: ${root}`);
  const projectName = readProjectName(root);
  log(args, `project: ${projectName}`);

  const config = loadConfig(root);
  const configRegexes = config.skipPatterns.map(configGlobToRegex);
  if (config.skipPatterns.length) log(args, `config: ${config.skipPatterns.length} skip patterns`);

  const allFiles = walk(root, configRegexes);
  log(args, `scanned: ${allFiles.length} files`);

  // Tree-sitter readiness: probe once. When the optional deps aren't installed this is
  // null and every AST language falls back to regex (or empty) — graceful degradation.
  const hasAstLang = allFiles.some((f) => isTreeSitterLang(LANG_BY_EXT[path.extname(f.rel).toLowerCase()] || 'other')
    || TREE_SITTER_TAG[LANG_BY_EXT[path.extname(f.rel).toLowerCase()] || 'other']);
  let treeSitterReady = false;
  if (hasAstLang) {
    treeSitterReady = (await initTreeSitter()) !== null;
    if (!treeSitterReady) {
      log(args, 'tree-sitter deps not installed — AST languages fall back to regex/empty. ' +
        'Run `npm install` in skills/update-codemap for full multi-language coverage.');
    }
  }

  const vendoredRegexes = config.vendoredPatterns.map(configGlobToRegex);
  const isVendored = (rel) => vendoredRegexes.some((re) => re.test(rel));

  const groups = new Map();
  const moduleImports = new Map();
  // Collected during the scan; feeds the C# namespace→file index that resolveImport
  // consults for `using` directives. Built after the loop so heads are read once.
  const namespaceByRel = new Map();

  for (const f of allFiles) {
    const ext = path.extname(f.rel).toLowerCase();
    const lang = LANG_BY_EXT[ext] || 'other';
    const head = readHead(f.abs, READ_CAP_LINES);
    if (isGenerated(head.lines)) continue;

    const vendored = isVendored(f.rel);

    let exports = [];
    let classes = [];
    let text = head.text;
    let astImports = null; // non-null when the tree-sitter engine supplied imports

    const tsTag = TREE_SITTER_TAG[lang];
    if (!vendored && tsTag && treeSitterReady) {
      // AST languages get the FULL file — declarations past the head-cap matter.
      const full = readFull(f.abs);
      const ast = await extractWithTreeSitter(full, tsTag);
      if (ast) {
        text = full;
        exports = ast.exports;
        classes = ast.classes;
        astImports = ast.imports;
      }
    }

    // Regex extraction: primary for ts/csharp, and the fallback for cpp when the AST
    // engine was unavailable or returned null (classes still empty here).
    if (!vendored && classes.length === 0 && exports.length === 0 &&
        (lang === 'ts' || lang === 'csharp' || lang === 'cpp-header')) {
      if (head.totalLines > READ_CAP_LINES && text === head.text) {
        const extended = readHead(f.abs, READ_CAP_EXTENDED);
        text = extended.text;
      }
      if (lang === 'ts') { exports = extractExportsTs(text); classes = extractClassesTs(text); }
      else if (lang === 'csharp') { exports = extractExportsCSharp(text); classes = extractClassesCSharp(text); }
      else if (lang === 'cpp-header') { exports = extractExportsCppHeader(text); classes = extractClassesCpp(text); }
    }

    const purpose = vendored ? '<vendored>' : derivePurpose(head.lines, f.rel);
    const top = topLevelDirOf(f.rel);
    if (!groups.has(top)) groups.set(top, []);
    groups.get(top).push({ rel: f.rel, purpose, exports, classes, vendored });

    if (!vendored) {
      // Prefer AST-extracted imports; else regex. (cpp-impl has no AST classes but its
      // includes still come through the engine when ready.)
      const targets = astImports !== null ? astImports : extractImports(text, lang);
      if (targets.length) moduleImports.set(f.rel, targets);
    }

    // C# namespace declaration → index entry, so `using` directives in other files
    // resolve to this file's module. Non-vendored .cs only; a file may declare a
    // namespace without exporting a class, so this is independent of `classes`.
    if (!vendored && lang === 'csharp') {
      const nsMatch = text.match(/^\s*namespace\s+([A-Za-z_][\w.]*)\s*[;{]/m);
      if (nsMatch) namespaceByRel.set(f.rel, nsMatch[1]);
    }
  }

  const fileIndex = buildFileIndex(allFiles, namespaceByRel);
  const topLevelDirs = new Set(groups.keys());

  // Edges. Two layers:
  //   moduleEdges[from][to] = count           (weighted, for ## Dependencies)
  //   fileEdges[from] = Set<targetRel>        (for ## File deps)
  const moduleEdges = new Map();
  const fileEdges = new Map();
  for (const [sourceRel, targets] of moduleImports.entries()) {
    const sourceMod = topLevelDirOf(sourceRel);
    for (const t of targets) {
      const resolved = resolveImport(t, sourceRel, topLevelDirs, fileIndex);
      if (!resolved) continue;
      const targetMod = resolved.mod;
      if (!targetMod) continue;
      if (resolved.rel && resolved.rel !== sourceRel) {
        if (!fileEdges.has(sourceRel)) fileEdges.set(sourceRel, new Set());
        fileEdges.get(sourceRel).add(resolved.rel);
      }
      if (targetMod === sourceMod) continue;
      if (!moduleEdges.has(sourceMod)) moduleEdges.set(sourceMod, new Map());
      const bucket = moduleEdges.get(sourceMod);
      bucket.set(targetMod, (bucket.get(targetMod) || 0) + 1);
    }
  }

  const entryPoints = detectEntryPoints(root, allFiles);

  const today = new Date().toISOString().slice(0, 10);

  // ----- LLM-facing prose header (Overview, Modules, Conventions, Hubs).
  // Section ordering is load-bearing: prose first, then the structured sections
  // consumed by codemap-visualize. The visualize parser keys on `## <name>/`
  // for groups and on a fixed set of named sections (Entry points, Layers,
  // Dependencies, File deps, Class graph) — anything else is benign noise.
  const inboundCounts = computeInboundCounts(fileEdges);
  const importedBy = computeModuleImportedBy(moduleEdges);
  const readmeH1 = readReadmeH1(root);
  const pkgDesc = readPackageDescription(root);
  const domLang = dominantLanguage(groups);

  let out = `# Architecture — ${projectName}\nLast updated: ${today}\n\n`;

  // ## Overview
  const overviewParts = [];
  const ensurePeriod = (s) => /[.!?]$/.test(s.trim()) ? s.trim() : (s.trim() + '.');
  if (readmeH1) overviewParts.push(ensurePeriod(readmeH1));
  else if (pkgDesc) overviewParts.push(ensurePeriod(pkgDesc));
  else overviewParts.push(`${projectName} — codemap-derived architecture summary.`);
  if (entryPoints.length) {
    overviewParts.push(`Entry: ${entryPoints.slice(0, 2).map((e) => `\`${e.rel}\``).join(', ')}.`);
  }
  if (domLang) overviewParts.push(`Dominant language: ${domLang}.`);
  out += `## Overview\n${overviewParts.join(' ')}\n\n`;

  // ## Modules
  const sortedModuleNames = [...groups.keys()].sort();
  if (sortedModuleNames.length) {
    out += `## Modules\n`;
    for (const modName of sortedModuleNames) {
      const items = groups.get(modName);
      const role = inferModuleRole(modName, items);
      const purpose = inferModulePurpose(items, inboundCounts);
      const entry = findModuleEntry(modName, items, entryPoints);
      const surface = publicSurfaceForModule(items, inboundCounts);
      const depsOn = moduleEdges.has(modName)
        ? [...moduleEdges.get(modName).keys()].sort()
        : [];
      const impBy = importedBy.has(modName)
        ? [...importedBy.get(modName)].sort()
        : [];
      const internals = computeInternals(modName, items, inboundCounts);

      out += `### ${modName}\n`;
      out += `**Role:** ${role}\n`;
      if (purpose) out += `**Purpose:** ${purpose}\n`;
      if (entry) out += `**Entry:** \`${entry}\`\n`;
      if (surface.length) out += `**Public surface:** ${surface.map((s) => `\`${s}\``).join(', ')}\n`;
      if (depsOn.length) out += `**Depends on:** ${depsOn.join(', ')}\n`;
      if (impBy.length) out += `**Imported by:** ${impBy.join(', ')}\n`;
      if (internals.length) {
        out += `**Internals:**\n`;
        for (const line of internals) out += `- ${line}\n`;
      }
      out += `\n`;
    }
  }

  // ## Conventions detected — only fire for ≥3-instance explicit markers.
  const conventions = detectConventions(groups);
  if (conventions.length) {
    out += `## Conventions detected\n`;
    for (const c of conventions) {
      out += `- ${c.name}: ${c.count} instances — ${c.detail}\n`;
    }
    out += `\n`;
  }

  // ## Hubs — files with ≥3 inbound file-edges, ranked by importer count.
  const hubEntries = [];
  for (const [file, count] of inboundCounts.entries()) {
    if (count < 3) continue;
    // Find purpose by scanning groups.
    let purpose = '';
    for (const items of groups.values()) {
      const hit = items.find((it) => it.rel === file);
      if (hit) { purpose = hit.purpose; break; }
    }
    hubEntries.push({ file, count, purpose });
  }
  hubEntries.sort((a, b) => b.count - a.count || a.file.localeCompare(b.file));
  if (hubEntries.length) {
    out += `## Hubs\n`;
    for (const h of hubEntries) {
      const tail = h.purpose ? `, role: ${h.purpose}` : '';
      out += `- \`${h.file}\` — ${h.count} importers${tail}\n`;
    }
    out += `\n`;
  }

  if (entryPoints.length) {
    out += `## Entry points\n`;
    for (const e of entryPoints) out += `- \`${e.rel}\` — ${e.role}\n`;
    out += `\n`;
  }
  if (config.layers.length) {
    out += `## Layers\n`;
    for (const l of config.layers) out += `- ${l.name}: ${l.modules.join(', ')}\n`;
    out += `\n`;
  }
  if (moduleEdges.size) {
    out += `## Dependencies\n`;
    const sortedSources = [...moduleEdges.keys()].sort();
    for (const s of sortedSources) {
      // Sort by weight desc, then name asc, and render `target (N)`.
      const entries = [...moduleEdges.get(s).entries()]
        .sort((a, b) => (b[1] - a[1]) || a[0].localeCompare(b[0]));
      const rendered = entries.map(([t, w]) => `${t} (${w})`).join(', ');
      out += `- ${s} → ${rendered}\n`;
    }
    out += `\n`;
  }
  if (fileEdges.size) {
    out += `## File deps\n`;
    const sortedSources = [...fileEdges.keys()].sort();
    for (const s of sortedSources) {
      const targets = [...fileEdges.get(s)].sort();
      out += `- \`${s}\` → ${targets.map((t) => `\`${t}\``).join(', ')}\n`;
    }
    out += `\n`;
  }

  // Phase 1c: ## Class graph — class-level data for UML rendering and convention detection.
  // Block-per-class shape: parser keys off "- class:" / "- interface:" / "- struct:" / "- record:" / "- enum:".
  // Sub-lines (indented 2 spaces) carry attributes; each starts with a stable keyword followed by ":".
  const allClassEntries = [];
  for (const [g, items] of groups.entries()) {
    for (const it of items) {
      if (!it.classes || !it.classes.length) continue;
      for (const c of it.classes) {
        allClassEntries.push({ file: it.rel, module: g, ...c });
      }
    }
  }
  if (allClassEntries.length) {
    out += `## Class graph\n`;
    const sorted = allClassEntries.sort((a, b) => a.file.localeCompare(b.file) || a.name.localeCompare(b.name));
    for (const c of sorted) {
      out += `- ${c.kind}: \`${c.name}\` in \`${c.file}\`\n`;
      if (c.namespace) out += `  namespace: ${c.namespace}\n`;
      if (c.extends.length) out += `  extends: ${c.extends.join(' ; ')}\n`;
      if (c.implements.length) out += `  implements: ${c.implements.join(' ; ')}\n`;
      if (c.attributes.length) out += `  attributes: ${c.attributes.join(' ; ')}\n`;
      if (c.fields.length) {
        const renderedFields = c.fields.map((f) => `${f.name}: ${f.type}`).join(' ; ');
        out += `  fields: ${renderedFields}\n`;
      }
    }
    out += `\n`;
  }

  const sortedGroups = [...groups.keys()].sort();
  for (const g of sortedGroups) {
    out += `## ${g}\n`;
    const items = groups.get(g).sort((a, b) => a.rel.localeCompare(b.rel));
    for (const it of items) {
      out += `- \`${it.rel}\` — ${it.purpose}\n`;
      if (it.exports.length) {
        // Separator is ` ; ` (not `, `) because TS/C# signatures contain commas
        // inside parameter lists, generics, and tuple types. Visualize splits on the same token.
        out += `  exports: ${it.exports.map((e) => e.name).join(' ; ')}\n`;
      }
    }
    out += `\n`;
  }

  const codemapPath = path.join(root, '.claude', 'codemap.md');
  const dirtyPath = path.join(root, '.claude', 'codemap.dirty');
  let dirtyClearedFrom = null;
  if (fs.existsSync(dirtyPath)) {
    try { dirtyClearedFrom = fs.readFileSync(dirtyPath, 'utf8').trim(); } catch {}
  }

  if (args.dryRun) {
    log(args, '--- dry-run output ---');
    process.stdout.write(out);
    log(args, '--- end ---');
    return;
  }

  fs.mkdirSync(path.dirname(codemapPath), { recursive: true });
  const prev = loadPreviousCodemap(codemapPath);
  fs.writeFileSync(codemapPath, out);
  if (dirtyClearedFrom !== null) {
    try { fs.unlinkSync(dirtyPath); } catch {}
  }

  const diff = diffSummary(prev, out);
  if (diff.initial) {
    const exported = [...groups.values()].flat().filter((it) => it.exports.length).length;
    log(args, `initial scan: ${allFiles.length} files, ${exported} with exports across ${sortedGroups.length} top-level groups`);
  } else {
    log(args, `+ ${diff.added} files added`);
    log(args, `- ${diff.removed} files removed`);
  }
  if (config.skipPatterns.length) log(args, `config applied: ${config.skipPatterns.length} skip patterns from .claude/codemap.config.md`);
  if (config.warnings.length) {
    log(args, `warnings (malformed config lines, ignored):`);
    for (const w of config.warnings) log(args, `  - line ${w.line}: ${w.text}`);
  }
  if (dirtyClearedFrom !== null) log(args, `dirty sentinel cleared (last marked: ${dirtyClearedFrom})`);
  log(args, `wrote ${path.relative(root, codemapPath)}`);
}

main().catch((err) => { console.error(err); process.exit(1); });
