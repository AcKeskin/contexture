// These build the # Architecture header (Overview, Modules, Conventions, Hubs)
// that ships above the structured sections consumed by codemap-visualize.

function readReadmeH1(root) {
  try {
    const readme = path.join(root, 'README.md');
    if (!fs.existsSync(readme)) return '';
    const text = fs.readFileSync(readme, 'utf8');
    const lines = text.split(/\r?\n/);
    const h1Index = lines.findIndex((l) => /^#\s+/.test(l));
    if (h1Index < 0) return '';
    // First paragraph after H1, skipping blank lines.
    const para = [];
    for (let i = h1Index + 1; i < Math.min(lines.length, h1Index + 30); i++) {
      const l = lines[i].trim();
      if (!l) { if (para.length) break; else continue; }
      if (/^#/.test(l)) break;
      para.push(l);
    }
    return para.join(' ').trim();
  } catch { return ''; }
}

function readPackageDescription(root) {
  try {
    const pkg = path.join(root, 'package.json');
    if (fs.existsSync(pkg)) {
      const j = JSON.parse(fs.readFileSync(pkg, 'utf8'));
      if (j.description) return String(j.description).trim();
    }
  } catch {}
  // Walk one level deep for a nested package.json description (e.g. server/package.json).
  try {
    const entries = fs.readdirSync(root, { withFileTypes: true });
    for (const ent of entries) {
      if (!ent.isDirectory() || SKIP_DIRS.has(ent.name)) continue;
      const p = path.join(root, ent.name, 'package.json');
      if (fs.existsSync(p)) {
        try {
          const j = JSON.parse(fs.readFileSync(p, 'utf8'));
          if (j.description) return String(j.description).trim();
        } catch {}
      }
    }
  } catch {}
  return '';
}

function dominantLanguage(groups) {
  const counts = new Map();
  for (const items of groups.values()) {
    for (const it of items) {
      const ext = path.extname(it.rel).toLowerCase();
      const lang = LANG_BY_EXT[ext];
      if (!lang) continue;
      counts.set(lang, (counts.get(lang) || 0) + 1);
    }
  }
  const sorted = [...counts.entries()].sort((a, b) => b[1] - a[1]);
  return sorted.length ? sorted[0][0] : '';
}

