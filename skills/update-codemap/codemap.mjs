function readProjectName(root) {
  try {
    const readme = path.join(root, 'README.md');
    if (fs.existsSync(readme)) {
      const first = fs.readFileSync(readme, 'utf8').split(/\r?\n/).find((l) => /^#\s+/.test(l));
      if (first) return first.replace(/^#\s+/, '').trim();
    }
  } catch {}
  try {
    const pkg = path.join(root, 'package.json');
    if (fs.existsSync(pkg)) {
      const j = JSON.parse(fs.readFileSync(pkg, 'utf8'));
      if (j.name) return String(j.name).trim();
    }
  } catch {}
  try {
    const csprojs = fs.readdirSync(root).filter((f) => f.endsWith('.csproj'));
    if (csprojs.length) {
      const x = fs.readFileSync(path.join(root, csprojs[0]), 'utf8');
      const m = x.match(/<AssemblyName>([^<]+)<\/AssemblyName>/);
      if (m) return m[1].trim();
    }
  } catch {}
  return path.basename(root);
}

function loadConfig(root) {
  const cfgPath = path.join(root, '.claude', 'codemap.config.md');
  const out = { skipPatterns: [], layers: [], vendoredPatterns: [], autoUpdateEnabled: false, warnings: [] };
  if (!fs.existsSync(cfgPath)) return out;
  const lines = fs.readFileSync(cfgPath, 'utf8').split(/\r?\n/);
  let section = null;
  let lineNo = 0;
  for (const raw of lines) {
    lineNo++;
    const line = raw;
    const h = line.match(/^##\s+(.+?)\s*$/);
    if (h) { section = h[1].trim(); continue; }
    if (!section) continue;
    if (section === 'Skip') {
      const m = line.match(/^\s*-\s*`([^`]+)`\s*$/);
      if (m) out.skipPatterns.push(m[1]);
      else if (/^\s*-\s*`/.test(line)) out.warnings.push({ line: lineNo, text: line });
    } else if (section === 'Layers') {
      const m = line.match(/^\s*-\s*([^:]+):\s*(.+)$/);
      if (m) out.layers.push({ name: m[1].trim(), modules: m[2].split(',').map((s) => s.trim()).filter(Boolean) });
    } else if (section === 'Vendored') {
      const m = line.match(/^\s*-\s*`([^`]+)`\s*$/);
      if (m) out.vendoredPatterns.push(m[1]);
      else if (/^\s*-\s*`/.test(line)) out.warnings.push({ line: lineNo, text: line });
    } else if (section === 'Auto-update') {
      if (/^\s*-?\s*enabled\s*:\s*true\s*$/i.test(line)) out.autoUpdateEnabled = true;
    }
  }
  return out;
}

function configGlobToRegex(pattern) {
  // gitignore-style: trailing / matches dir, ** crosses segments, * single segment.
  const isDir = pattern.endsWith('/');
  const p = isDir ? pattern.slice(0, -1) : pattern;
  let re = '';
  let i = 0;
  while (i < p.length) {
    const c = p[i];
    if (c === '*' && p[i + 1] === '*') { re += '.*'; i += 2; }
    else if (c === '*') { re += '[^/]*'; i++; }
    else if (c === '?') { re += '[^/]'; i++; }
    else if (/[.+^${}()|[\]\\]/.test(c)) { re += '\\' + c; i++; }
    else { re += c; i++; }
  }
  if (isDir) re += '(/.*)?';
  return new RegExp('(^|/)' + re + '$');
}

function walk(root, configSkipRegexes) {
  const files = [];
  function recur(dir) {
    let entries;
    try { entries = fs.readdirSync(dir, { withFileTypes: true }); } catch { return; }
    for (const ent of entries) {
      const abs = path.join(dir, ent.name);
      const rel = path.relative(root, abs).replace(/\\/g, '/');
      if (ent.isDirectory()) {
        if (SKIP_DIRS.has(ent.name)) continue;
        if (configSkipRegexes.some((re) => re.test(rel + '/'))) continue;
        recur(abs);
      } else if (ent.isFile()) {
        if (SKIP_FILENAMES.has(ent.name)) continue;
        if (SKIP_FILENAME_PREFIXES.some((p) => ent.name.startsWith(p))) continue;
        if (SKIP_FILE_PATTERNS.some((re) => re.test(ent.name))) continue;
        if (configSkipRegexes.some((re) => re.test(rel))) continue;
        files.push({ abs, rel });
      }
    }
  }
  recur(root);
  return files;
}

